class Command(BaseCommand):
    help = "Засеять начальные данные: серии гайдов и словарь"

    def handle(self, *args, **options):
        from apps.meta.models import Role

        # Glossary
        created = 0
        for term, slug, definition, example in GLOSSARY:
            _, c = GlossaryTerm.objects.get_or_create(
                slug=slug, defaults={"term": term, "definition": definition, "example": example}
            )
            if c:
                created += 1
        self.stdout.write(f"  Словарь: {created} терминов")

        # Series + Guides
        guide_count = 0
        for s in SERIES_DATA:
            role = Role.objects.filter(slug=s["role"]).first() if s["role"] else None
            series, _ = GuideSeries.objects.get_or_create(
                slug=s["slug"],
                defaults={
                    "title": s["title"],
                    "description": s["description"],
                    "role": role,
                    "order": s["order"],
                },
            )
            for g in s["guides"]:
                _, c = Guide.objects.get_or_create(
                    slug=g["slug"],
                    defaults={
                        "series": series,
                        "title": g["title"],
                        "difficulty": g["difficulty"],
                        "published": g["published"],
                        "excerpt": g.get("excerpt", ""),
                        "content": g["content"],
                    },
                )
                if c:
                    guide_count += 1

        self.stdout.write(f"  Гайды: {guide_count} создано")
        self.stdout.write(self.style.SUCCESS("Гайды засеяны!"))
